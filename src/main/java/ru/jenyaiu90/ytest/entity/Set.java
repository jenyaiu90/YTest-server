package ru.jenyaiu90.ytest.entity;

//Задание для группы
public class Set
{
	protected int id;
	protected int group;
	protected int test;

	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getGroup()
	{
		return group;
	}
	public void setGroup(int group)
	{
		this.group = group;
	}
	public int getTest()
	{
		return test;
	}
	public void setTest(int test)
	{
		this.test = test;
	}
}
